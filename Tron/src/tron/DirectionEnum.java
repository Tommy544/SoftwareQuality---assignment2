/**
 *
 * @author vcaniga
 */
public enum DirectionEnum {
    UP,
    DOWN,
    LEFT,
    RIGHT;
    
    public static DirectionEnum turnRight(DirectionEnum direction) {
        switch (direction) {
            case UP:
                return RIGHT;
            case RIGHT:
                return DOWN;
            case DOWN:
                return LEFT;
            case LEFT:
                return UP;
            default:
                return null;
        }
    }
    
    public static DirectionEnum turnLeft(DirectionEnum direction) {
        switch (direction) {
            case UP:
                return LEFT;
            case RIGHT:
                return UP;
            case DOWN:
                return RIGHT;
            case LEFT:
                return DOWN;
            default:
                return null;
        }
    }
}
